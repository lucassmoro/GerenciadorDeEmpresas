    private void OkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OkActionPerformed
        // TODO add your handling code here:
        
        try {
            int indexArea = areaCombo.getSelectedIndex();
            empresa = new Empresa(NomeText.getText(), cnpjText.getText(),AreaDeAtividade.values()[indexArea], enderecoEmpresa);
            JOptionPane.showMessageDialog(this, "Empresa registrada com sucesso. \n" + empresa.toString(), "Alerta", JOptionPane.INFORMATION_MESSAGE);
            dispose();
            }
        catch (HeadlessException ex) {
            Logger.getLogger(NovaEmpresaInterface.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Preencha todos os campos");
           }
        catch (Exception ex) {
            Logger.getLogger(NovaEmpresaInterface.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Ocorreu um erro ao registrar a empresa: " + ex.getMessage());
            }
        
        
    }//GEN-LAST:event_OkActionPerformed

