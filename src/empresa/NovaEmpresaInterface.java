    private void pontinhosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pontinhosActionPerformed
        // TODO add your handling code here:
        DialogEndereco dialog = new DialogEndereco(null, true);
        if (empresa != null) {
            dialog.setModel(empresa.getEndereco());
        }
        dialog.pack();
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true);
        // a janela de diálogo modal foi escondida
        Endereco retorno = dialog.getModel();
        if (retorno != null) {
            enderecoEmpresa = retorno;
            enderecoText.setText(retorno.toString());
        }
        
    }//GEN-LAST:event_pontinhosActionPerformed

