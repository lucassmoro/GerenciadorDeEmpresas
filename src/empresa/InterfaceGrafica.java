    private void ArquivoNovoEnable(){
        boolean arquivo = arquivoAtual != null;
        EmpresaButton.setEnabled(true);
        fecharButton.setEnabled(true);
        abrirButton.setEnabled(true);
        newCliente.setEnabled(true);
        newSetor.setEnabled(true);
        newFuncionario.setEnabled(true);
        if (!arquivo) {
            this.setTitle("Gerenciador de Empresas");
        } else {
            this.setTitle("Gerenciador de Empresas {" + arquivoAtual.getName() + "}");

        }
    }
    
