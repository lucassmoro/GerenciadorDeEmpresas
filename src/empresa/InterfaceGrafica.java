    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(arquivoAtual);
        fileChooser.setSelectedFile(arquivoAtual);
        int result = fileChooser.showSaveDialog(jMenu1);
        if (result == JFileChooser.APPROVE_OPTION) {
            arquivoAtual = fileChooser.getSelectedFile();
            // serializa os objetos e salva empresas no arquivo
            for (Empresa empresa : empresaLista) {
                try {
                    empresa.Salvar(arquivoAtual);
                } catch (IOException ex) {
                    Logger.getLogger(InterfaceGrafica.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, "Erro ao salvar o arquivo: " + ex.getMessage(), "ERRO", JOptionPane.WARNING_MESSAGE);
                } catch (Exception ex) {
                    Logger.getLogger(InterfaceGrafica.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        
    }//GEN-LAST:event_salvarButtonActionPerformed

    private void abrirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abrirButtonActionPerformed
        // TODO add your handling code here:
        if (!empresaLista.isEmpty()) {
            if (JOptionPane.NO_OPTION == JOptionPane.showConfirmDialog(this, "Já existem empresas. Deseja sobrescrevê-las?", "Abrir Empresas", JOptionPane.YES_NO_OPTION)) {
                return;
            }
        }
        // use JFileChooser (seção 15.6) para permitir que o usuário abra um arquivo com empresas gravadas nele 
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            arquivoAtual = fileChooser.getSelectedFile();
            //boolean acabou = false;
            empresaLista.clear();
            //while (!acabou) {
                try {
                    empresaAtual = Empresa.Ler(arquivoAtual);
                    this.empresaLista.add(empresaAtual);
                    ArquivoNovoEnable();
                    EmpresaNovaEnable();
                    EditEmpresa.setEnabled(true);
                } catch (IOException ex) {
                    Logger.getLogger(InterfaceGrafica.class.getName()).log(Level.SEVERE, null, ex);
                    //acabou = true;
                }
            }
        //}
    }//GEN-LAST:event_abrirButtonActionPerformed

